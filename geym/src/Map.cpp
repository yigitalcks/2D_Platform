Map::Map() {
	dirt = TextureManager::LoadTexture("assets/dirt.png");
	grass = TextureManager::LoadTexture("assets/grass.png");

	initMap(lvl1);

	srcR.x = destR.x = 0;
	srcR.y = destR.y = 0;
	srcR.w = destR.w = 40;
	srcR.h = destR.h = 40;

}
Map::~Map() {
	TextureManager::clearTexture(dirt);
	TextureManager::clearTexture(grass);
}

void Map::initMap(int arr[18][32]) {
	for (int i = 0; i < 18; i++) {
		for (int j = 0; j < 32; j++) {
			map[i][j] = arr[i][j];
		}
	}
}

void Map::renderMap() {
	for (int i = 0; i < 18; i++) {
		destR.y = i * 40;
		for (int j = 0; j < 32; j++) {
			destR.x = j * 40;

			int textType = map[i][j];

			switch (textType)
			{
			case 1:
				TextureManager::draw(grass, &srcR, &destR);
				break;
			case 0:
				TextureManager::draw(dirt, &srcR, &destR);
				break;
			default:
				break;
			}
		}
	}
}